def typeout(text,speed):
    for char in text:
        pyautogui.typewrite(char)
        time.sleep(speed)

def change_all():
    pyautogui.hotkey('ctrl', 'a')
    pyautogui.hotkey('ctrl', 'v')



def shift_text(text):
    text =  indent(text, '                 ')
    pyperclip.copy(text)
    change_all()
    time.sleep(.1)
    time.sleep(.2)

def display(text):
    time.sleep(1)
    pyperclip.copy(text)
    pyautogui.hotkey('ctrl', 'v')
    pyautogui.hotkey('ctrl', 'a')
    time.sleep(1)


